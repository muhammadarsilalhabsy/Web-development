document.getElementById() // mencari ID, dan mengembalikan satu element  (karena id === unik)

document.getElementsByClassName() // mencari class, dan akan mengembalikan HTMLCollection

document.getElementsByTagName() // mencari tag html (p, h1, ect), dan akan mengembalikan HTMLCollection juga

document.querySelector() // akan mengembalikan sebuah element

document.querySelectorAll() // akan mengembalikan NodeList


